#include "../includes/libft.h"
#include "stdlib.h"

void	ft_arrdel(char ***arr, int x)
{
	int i;

	i = -1;
	while (++i < x)
	{
		ft_strdel(&(*arr)[i]);
	}
	*arr = NULL;
}
